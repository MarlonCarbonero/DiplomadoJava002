    private void lstMarcaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstMarcaValueChanged
        // TODO add your handling code here:
        String marca = lstMarca.getSelectedValue();

        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        switch (marca) {
            case "BMW":
                modelo.addElement("BMW 328");
                modelo.addElement("BMW 507");
                break;
            case "TOYOTA":
                modelo.addElement("COROLLA");
                modelo.addElement("YARIS");
                break;
            case "NISSAN":
                modelo.addElement("SENTRA");
                modelo.addElement("VERSA");
                break;
            case "HYUNDAI":
                modelo.addElement("ACCENT");
                modelo.addElement("ELANTRA");
                break;
        }
        this.cbModelo.setModel(modelo);
    }//GEN-LAST:event_lstMarcaValueChanged

