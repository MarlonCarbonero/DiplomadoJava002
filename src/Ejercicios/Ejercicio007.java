    public void transmisionValue() {
        if (this.jRadioButton1.isSelected()) {
            total += 15;
        }
        if (this.jRadioButton2.isSelected()) {
            total += 20;
        }
    }

    public void sliderValue() {
        if (jSlider1.getValue() < 31) {
            int subtotal = jSlider1.getValue() * 25;
            total += subtotal;
        }

    }

    public void totalModelo() {
        if ((cbModelo.getSelectedItem()).equals("BMW 328")) {
            total += 5;
            total = total * 1.13;
        } else if ((cbModelo.getSelectedItem()).equals("bmw 507") || (cbModelo.getSelectedItem()).equals("ACCENT") || (cbModelo.getSelectedItem()).equals("VERSA")) {
            total += 10;

            total = total * 1.13;
        } else if ((cbModelo.getSelectedItem()).equals("ELANTRA") || (cbModelo.getSelectedItem()).equals("SENTRA")) {
            total += 15;
            total = total * 1.13;
        } else if ((cbModelo.getSelectedItem()).equals("COROLLA")) {
            total += 20;
            total = total * 1.13;
        } else if ((cbModelo.getSelectedItem()).equals("YARIS")) {
            total += 25;
            total = total * 1.13;
        }
    }

    public void totalMarca() {
        DecimalFormat df = new DecimalFormat("#.##");

        if ((this.lstMarca.getSelectedValue().toString()).equals("BMW")) {
            total += 50;
            addExtra();
            sliderValue();
            transmisionValue();
            totalModelo();
            this.jLabel9.setText("$" + df.format(total));
        } else if ((this.lstMarca.getSelectedValue().toString()).equals("TOYOTA")) {
            total += 25;
            addExtra();
            sliderValue();
            transmisionValue();
            totalModelo();
            this.jLabel9.setText("$" + df.format(total));
        } else if ((this.lstMarca.getSelectedValue().toString()).equals("NISSAN")) {
            total += 20;
            addExtra();
            sliderValue();
            transmisionValue();
            totalModelo();
            this.jLabel9.setText("$" + df.format(total));
        } else if ((this.lstMarca.getSelectedValue().toString()).equals("HYUNDAI")) {
            total += 18;
            addExtra();
            sliderValue();
            transmisionValue();
            totalModelo();
            this.jLabel9.setText("$" + df.format(total));
        }

    }

    public void resetValue() {
        this.total = 0;
    }

